def is_apple_connect(guide: str) -> bool:
    return guide == "connect_apple"


def base_guide(guide: str) -> str:
    """Ключ для доступа к шагам (connect_apple использует шаги connect)."""
    return "connect" if guide == "connect_apple" else guide


def step_text(guide: str, step: int) -> str:
    steps = GUIDES.get(base_guide(guide), [])
    if 0 <= step < len(steps):
        return steps[step]
    return "…"


def step_count(guide: str) -> int:
    return len(GUIDES.get(base_guide(guide), []))


